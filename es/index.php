      <h2>El Rhino Agriwash Defender</h2>
      <h4>Sistema compacto de desinfección de alto rendimiento</h4>
      <p>El Rhino Agriwash Defender es un sistema automático de desinfección para vehículos.  Ha sido diseñado específicamente para reducir la propagación de enfermedades por los movimientos vehiculares en lugares donde la bioseguridad es una preocupación.</p>
      <div class="hidden">
		<p>El sistema se compone de una robusta plataforma de lavado con rampas de perfil bajo, barras de pulverización, pantallas laterales, un sensor de entrada, una unidad de dosificación de productos químicos de alto rendimiento con una bomba y un pequeño panel de control montado sobre un tanque de almacenamiento de químico de 110 litros de capacidad.  Es un sistema de montaje en superficie que requiere una sola fase de suministro de energía de 240v y requiere también una conexión de agua.</p>
		<p>El Defender se activa automáticamente cuando un vehículo pasa por el sensor de entrada. El sistema cuenta con boquillas estratégicamente colocadas sobre cuatro barras verticales de pulverización ubicadas en las esquinas de la unidad y barras de pulverización horizontales ubicadas en la base de la plataforma. Con ello se garantiza la cobertura eficaz de los neumáticos, ruedas y el chasis del vehículo. La bomba suministra una presión de 3 bares en la boquilla, asegurando una cobertura consistente sin importar las condiciones climáticas.. El ciclo de pulverización es ajustable para adaptarse a las necesidades individuales de cada cliente. El exceso de rociado se reduce al mínimo en virtud de las pantallas laterales fabricadas de un resistente PVC y que pueden ser removidas fácilmente si se cambia la localización del sistema.</p>
		<p>El agua residual se recoge en la plataforma de lavado de poca profundidad que cuenta con una válvula de desagüe. El agua residual puede vaciarse conectando una manguera a la válvula de desagüe para transferirla un recipiente externo. Como alternativa, y sólo cuando sea permisible, puede vertirse a la superficie o al sistema de alcantarillado si estuviera disponible.</p>
		<p>La unidad avanzada de dosificación suministrada con el Rhino Agriwash Defender ha sido seleccionada por su precisión y consistencia a largo plazo. La dosificación ajustable viene como estándar (de 0,2% a 5%) y se adapta fácilmente a cualquier desinfectante químico, acido o alcalino, comúnmente utilizado.</p>
        <p>El Rhino Agriwash Defender puede transportarse en un camión pequeño dotado con una grúa de descarga  y se puede instalar en tan solo una hora.</p>
